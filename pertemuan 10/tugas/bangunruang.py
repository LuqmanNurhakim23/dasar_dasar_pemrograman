import math

def kubus(sisi):
    return sisi**3

def  balok(p, l, t):
    return p*l*t

def prisma(luas, tinggi):
    return luas * tinggi

def tabung(jarijari, tinggi):
    return math.pi * jarijari**2 * tinggi

def kerucut(jarijari, tinggi):
    return (1/3) * math.pi * jarijari * jarijari * tinggi